<h3>Скачивание движка Drupal</h3>
<p>Пропустите данный шаг, если у вас уже есть скаченный/установленный движок Drupal.</p>
<p>Первым шагом развертывания сайта на базе Drupal является загрузка ядра Drupal с официального сайта <a
        href="https://drupal.org/download" target="_blank">http://drupal.org/</a>.</p>
<p>На открывшейся странице вы можете скачать zip- или tar.gz-архив с файлами ядра.</p>
<p>Для того, чтобы скачать последнюю версию Drupal, нажмите кнопку <strong>Download Drupal X.XX (Скачать Drupal
        X.XX)</strong>, где X.XX - номер последней версии Drupal.</p>
<figure class="img-polaroid">
    <img src="img/downloading-01.png" alt="Downloading Drupal Engine page">
</figure>
<p>В случае, если вам необходима более ранняя версия Drupal, перейдите по ссылке <strong>Other Releases (Другие
        версии)</strong>:</p>
<figure class="img-polaroid">
    <img src="img/downloading-03.png" alt="Downloading Drupal Engine other releases">
</figure>
<p>На открывшейся странице в поле <strong>API Version (Версия API)</strong> выберите нужную ветку версий Drupal и
    нажмите кнопку <strong>Apply (Применить)</strong>:</p>
<p>Ниже появится полный список версий в данной ветке, и вы сможете скачать нужную вам версию.</p>
<figure class="img-polaroid">
    <img src="img/downloading-04.png" alt="Downloading Drupal Engine other releases. Versions">
</figure>
<p class="alert alert-warning">Проверьте требования шаблона относительно требуемой версии Drupal и убедитесь, что вы
    скачиваете нужную версию.</p>