<h3>Извлечение файлов</h3>
<p>После загрузки Drupal вам потребуется извлечь файлы и папки из архива.</p>
<p>Вы можете сделать это с помощью программ <strong>WinZip</strong> (PC), <strong>StuffitExpander</strong> (MAC) и т.п.
</p>
<p>Посмотрите следующие инструкции по извлечению файлов из архивов <a
        href="/help/ru/extract-archived-files-windows-os.html" target="_blank">WinZip (PC)</a> и <a
        href="/help/ru/extract-archived-files-mac-os.html" target="_blank">StuffitExpander (MAC)</a>.</p>
<p class="alert alert-warning">Внимание! Если в панели управления вашим хостингом доступна функция <strong>"Распаковать
        файлы"</strong>, вы можете загрузить на сервер сам .zip архив и распаковать его там.
</p>