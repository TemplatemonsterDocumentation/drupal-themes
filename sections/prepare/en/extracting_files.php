<h3>Extracting files</h3>
<p>When you are done with the downloading, you need to extract the files and folders from the engine package.</p>
<p>You can extract files using any archive manager like <strong>WinZip</strong> (PC), <strong>StuffitExpander</strong>
    (MAC), etc.</p>
<p>The following tutorials will show you how to extract archive files using <a
        href="/help/ru/extract-archived-files-windows-os.html" target="_blank">WinZip (PC)</a> and <a
        href="/help/ru/extract-archived-files-mac-os.html" target="_blank">StuffitExpander (MAC)</a> application.</p>
<p class="alert alert-warning">
    Attention! If your hosting control panel includes the <strong>"Extract"</strong>option, you can upload .zip package to the server and extract files there.
</p>