<h3>Creating a database</h3>
<p>As it was mentioned above, Drupal installation requires MySQL database. Database can be created in your hosting
	control panel.</p>
<p>You can contact your hosting provider in order to get some help creating a database.</p>
<p>During the database creation process you will get the following details:</p>
<ul class="marked-list">
	<li>Database server name or hostname.</li>
	<li>Database name.</li>
	<li>Database username.</li>
	<li>Database password.</li>
</ul>
<p>Please, note these details. You will need them during the installation process.</p>
