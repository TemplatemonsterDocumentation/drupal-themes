<h3>Downloading Drupal engine</h3>
<p>In case the Drupal engine is already downloaded/installed, skip this step.</p>
<p>The first step of Drupal website building is the engine downloading from the official website <a
	href="https://drupal.org/download" target="_blank">http://drupal.org/</a>.</p>
<p>Using the link above you can download .zip or tar.gz archive with core files.</p>
<p>To download the latest Drupal version, click <strong>Download Drupal X.XX</strong> button, where X.XX is the latest
	Drupal version number.</p>
<figure class="img-polaroid">
	<img src="img/downloading-01.png" alt="Downloading Drupal Engine page">
</figure>
<p>If you are looking for an older Drupal version, follow the link <strong>Other Releases</strong>:</p>
<figure class="img-polaroid">
	<img src="img/downloading-03.png" alt="Downloading Drupal Engine other releases">
</figure>
<p>In the <strong>API Version</strong> field choose the required branch of Drupal versions and click
	<strong>Apply</strong> button:</p>
<p>Below you will see the full list of the selected branch versions, where you can download the required one.</p>
<figure class="img-polaroid">
	<img src="img/downloading-04.png" alt="Downloading Drupal Engine other releases. Versions">
</figure>
<p class="alert alert-warning">Please, check the template requirements regarding the engine version compatibility and
	make sure that you are downloading the correct version.</p>