<h3>Theme settings</h3>
<p>Every theme includes its own list of settings.</p>
<p>In order to change the default theme settings, follow the instructions below:</p>
<ol class="index-list">
	<li>
		In the admin panel navigate to <strong>Appearance</strong> menu or in a browser's address bar type <strong>http://www.example.com/?q=admin/appearance</strong>,
		where <strong>www.example.com</strong> is your website URL.
	</li>
	<li>
		<p>Click the <strong>Settings</strong> link under the name of your theme:</p>

		<figure class="img-polaroid">
			<img src="img/theme-settings-01.png" alt="Click theme settings">
		</figure>
	</li>
	<li>
		<p>In the <strong>General Settings</strong> tab you can set the following parameters:</p>

		<ul class="marked-list">
			<li>show/hide logo, sitename, slogan, user pictures in posts, user pictures in comments, user verification status in comments, shortcut icon, main and secondary menu;</li>
			<li>use the default theme logo or upload a custom one;</li>
			<li>use the default theme favicon or upload the custom one;</li>
			<li>stick the menu to the top of the browser window.</li>
		</ul>

		<figure class="img-polaroid">
			<img src="img/theme-settings-02.png" alt="General settings">
		</figure>
	</li>
	<li>
		<p>In the <strong>Breadcrumb Settings</strong> tab you can set the following parameters:</p>

		<ul class="marked-list">
			<li>show/hide breadcrumb;</li>
			<li>show/hide breadcrumb, if it only contains a link to the front page;</li>
			<li>show a separator between breadcrumbs.</li>
		</ul>

		<figure class="img-polaroid">
			<img src="img/theme-settings-03.png" alt="Breadcrumb Settings">
		</figure>
	</li>
	<li>
		<p>In the <strong>Regions Settings</strong>tab you can set the following parameters:</p>

		<ul class="marked-list">
			<li>set a background of the required region, and activate parallax effect;</li>
			<li>set a YouTube-video background of the required region and also specify start video time in seconds;</li>
			<li>make the region content fullwidth (e.g. remove grids).</li>
		</ul>

		<p class="alert alert-danger">
			<span>Some of that options require the <strong>TM Block Background</strong> module activated.</span>
		</p>

		<figure class="img-polaroid">
			<img src="img/theme-settings-06.png" alt="Regions Settings">
		</figure>
	</li>

	<li>
		<p>In the <strong>Blog Settings</strong> you can set the blog title. Leave this field empty to make the blog title
		be the same as the corresponding menu link title.</p>

		<figure class="img-polaroid">
			<img src="img/theme-settings-04.png" alt="Blog Settings">
		</figure>
	</li>
	<li>
		<p>In the <strong>Custom CSS</strong> tab you can add custom styles for the theme.</p>

		<p class="alert alert-warning">
			<span>This option refers to the purchased theme only. You cannot set any styles for other themes, such as Seven etc.</span>
		</p>

		<figure class="img-polaroid">
			<img src="img/theme-settings-05.png" alt="Custom CSS">
		</figure>
	</li>
</ol>