<h3>Using animation and configuring its settings</h3>
<p class="alert alert-info">
	<span>The <strong>TM Animate</strong> module controls the activation/deactivation of animation. Moreover, depending on where do you want to use animation, you may need the <strong>Block class</strong> or <strong>Views</strong> modules.</span>
</p>
<p>To add animation to a block, follow the steps below:</p>
<ol class="index-list">
	<li>
		In the admin panel navigate to <strong> Structure > Blocks</strong> menu or in a browser's address bar type
		<strong>http://www.example.com/?q=admin/structure/block</strong>, where <strong>www.example.com</strong> is your
		website URL.
	</li>
	<li>
		Click <strong>configure</strong> link next to the needed block.
	</li>
	<li>
		In the <strong>CSS class(es)</strong> field add 2 classes separated with space: <strong>
		<em>wow</em></strong>, to run animation when the block appears in the visibility, and the animation class.
	</li>
	<li>
		<p>Save the changes.</p>
		
		<figure class="img-polaroid">
			<img src="img/animate-settings-01.png" alt="Add animate classes">
		</figure>
	</li>
</ol>

<p>To add animation to a view, follow the steps below:</p>
<ol class="index-list">
	<li>
		In the admin panel navigate to <strong> Structure > Views</strong> menu or in a browser's address bar type
		<strong>http://www.example.com/?q=admin/structure/views</strong>, where <strong>www.example.com</strong> is your
		website URL.
	</li>
	<li>
		<p>Click <strong>edit</strong> link next to the needed view.</p>
		
		<p class="alert alert-info">
			<span>It works only for views with the <strong>Semantic Views</strong> format.</span>
		</p>
	</li>
	<li>
		<p>In the <strong>Format</strong> section click <strong>settings</strong> link.</p>

		<figure class="img-polaroid">
			<img src="img/animate-settings-02.png" alt="Click 'Settings'">
		</figure>
	</li>
	<li>
		<p>In the pop-up window in the <strong>Row</strong> section find <strong>Class attribute</strong> field and
		enter the animation class and <strong>wow</strong> class there.</p>

		<figure class="img-polaroid">
			<img src="img/animate-settings-03.png" alt="Enter classes">
		</figure>
	</li>
	<li>
		Press <strong>Apply</strong> button.
	</li>
	<li>
		<p>Save the view changes.</p>
		
		<p class="alert alert-info">
			<span>All available animation classes you can check on the site <a href="http://daneden.github.io/animate.css/">daneden.github.io</a></span>
		</p>
	</li>
</ol>