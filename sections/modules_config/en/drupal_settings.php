<h3>Drupal settings</h3>
<p>To check the full list of settings open <strong>Configuration</strong> menu in the admin panel or type <strong>http://www.example.com/?q=admin/config</strong>
    in a browser's address bar, where <strong>www.example.com</strong> is your website URL.</p>
<p>In order to change the default site settings, follow the instructions below:</p>
<ol class="index-list">
    <li>In the admin panel navigate to <strong>Configuration > System > Site information </strong> menu or in
        a browser's address bar type <strong>http://www.example.com/?q=admin/config/system/site-information</strong>,
        where <strong>www.example.com</strong> is your website URL.
    </li>
    <li>
        <p>Change the required parameters:</p>

        <ul class="marked-list">
            <li>site name;</li>
            <li>site slogan;</li>
            <li>site e-mail address;</li>
            <li>the number of blog posts on Blog page (or main page, if there are any posts attached to the main
                page);
            </li>
            <li>site main page URL;</li>
            <li>403 page's URL (access denied);</li>
            <li>404 page's URL (page not found).</li>
        </ul>
        <figure class="img-polaroid">
            <img src="img/drupal-settings-01.png" alt="Set site name">
        </figure>
    </li>
    <li>After you've made necessary changes, click <strong>Save configuration</strong> button.</li>
</ol>