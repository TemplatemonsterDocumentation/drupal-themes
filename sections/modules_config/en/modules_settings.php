<p>Each module contains its own settings. To set up the module, follow the steps below:</p>
<ol class="index-list">
    <li>Log in to Drupal website as administrator.</li>
    <li>In the admin panel navigate to <strong>Modules</strong> menu or in a browser's address bar type <strong>http://www.example.com/?q=admin/modules</strong>,
        where <strong>www.example.com</strong> is your site URL.
    </li>

    <li>Find the required module. You can use module filters to find the right one.</li>
    <li><p>Click <strong>Configure</strong> link next to the module title.</p>
        <figure class="img-polaroid">
            <img src="img/modules-settings-01.png" alt="Click configure link">
        </figure>
    </li>
    <li>Make necessary changes and save the settings.</li>
</ol>
