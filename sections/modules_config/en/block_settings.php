<h3>Configuring blocks</h3>
<p>Follow the instructions listed below to configure your blocks:</p>
<ol class="index-list">
    <li>Log in to Drupal website as administrator.</li>
    <li>In the admin panel navigate to <strong>Structure > Blocks</strong> menu or in a browser's address bar type
        <strong>http://www.example.com/?q=admin/structure/block</strong>, where <strong>www.example.com</strong> is your
        website URL.
    </li>
    <li><p>You can move blocks to different regions by using drag-and-drop method on this page:</p>
        <figure class="img-polaroid">
            <img src="img/block-settings-01.png" alt="Drag&amp;Drop blocks">
        </figure>
    </li>
    <li><p>You can move blocks using drop-down menus in the <strong>Region</strong> column:</p>
        <figure class="img-polaroid">
            <img src="img/block-settings-02.png" alt="Select block region">
        </figure>
    </li>
    <li><p>Each block settings can be configured separately by clicking <strong>configure</strong> link in the <strong>Operations</strong>
            column:</p>
        <figure class="img-polaroid">
            <img src="img/block-settings-03.png" alt="Configure block">
        </figure>
    </li>
    <li>
        <p>The following parameters can be set on the block settings page:</p>

        <ul class="marked-list">
            <li>block title;</li>
            <li>custom CSS class for this block;</li>
            <li>block's region for each theme separately;</li>
            <li>visibility settings (for certain pages, specific content types, specific user roles and separate
                users);
            </li>
            <li>block background setting (similarly to the region settings in the theme options).</li>
        </ul>
        <figure class="img-polaroid">
            <img src="img/block-settings-04.png" alt="Block settings page">
        </figure>

        <p class="alert alert-warning">
            <span>Keep in mind that block occupies the width of the region it is set to. To make the block with parallax effect or with video occupy the full width, put it to the region with the <strong>Fullwidth</strong> option enabled.</span>
        </p>
        <p class="alert alert-info">
            <span>Some blocks may include additional settings:</span>
        </p>
        <p></p>
    </li>
    <li><p>You can also open the block settings page directly from your site front-end. To do it, please, mouse over
            the
            needed block, click the gear wheel icon at the top right corner of the block and choose the <strong>Configure
                block</strong> option. This option is available only for the logged in users with the administrator
            rights:</p>
        <figure class="img-polaroid">
            <img src="img/block-settings-05.png" alt="Click cog to configure block">
        </figure>
    </li>

</ol>