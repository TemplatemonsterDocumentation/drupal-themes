<h3>Configuring social icons</h3>
<p class="alert alert-info">
	<span>The <strong>TM Font Awesome</strong> and <strong>TM Follow Us</strong> modules control the activation/deactivation of the social icons block.</span>
</p>
<p>To configure the social icons block settings, follow the steps below:</p>
<ol class="index-list">
	<li>
		<p>In the admin panel navigate to <strong>Structure > Blocks</strong> menu or in a browser's address bar
			type <strong>http://www.example.com/?q=admin/structure/block</strong>, where
			<strong>www.example.com</strong> is your website URL.</p>

		<p>You can open these settings by selecting <strong>Configure block</strong> option from the contextual menu of
			the social icons block.</p>

		<figure class="img-polaroid">
			<img src="img/follow-settings-03.png" alt="Choose 'Configure block'">
		</figure>
	</li>
	<li>
		To show/hide this block on user pages, check/uncheck <strong>User pages</strong> option.
	</li>
	<li>
		To show/hide social networks labels near icons, check/uncheck <strong>Display labels</strong> option.
	</li>
	<li>
		To display icons vertically/horizontally, select the required value in the <strong>Alignment</strong> field.
	</li>
	<li>
		<p>After all changes are done, press <strong>Save block </strong> button.</p>
		
		<figure class="img-polaroid">
			<img src="img/follow-settings-04.png" alt="Social sharing settings">
		</figure>
	</li>
</ol>

<p>To configure additional display parameters for social icons, follow the steps below:</p>
<ol class="index-list">
	<li>
		<p>In the admin panel navigate to <strong>Configuration > TM Follow</strong> menu or in a browser's address
			bar type <strong>http://www.example.com/?q=admin/config/tm_follow</strong>, where
			<strong>www.example.com</strong> is your website URL.</p>

		<p>You can open these settings by selecting <strong>Edit TM Follow links</strong> option from the contextual
			menu of the social icons block.</p>

		<figure class="img-polaroid">
			<img src="img/follow-settings-02.png" alt="Choose 'Edit TM Follow links'">
		</figure>
	</li>
	<li>
		To hide any social icon on the site, remove link from the <strong>URL</strong> field next to the needed social
		network.
	</li>
	<li>
		To display any social icon on the site, insert the link to your social media profile to the <strong>URL</strong>
		field.
	</li>
	<li>
		To change a social icon name, specify a new title in the <strong>Customized name</strong> next to the needed
		module.
	</li>
	<li>
		<p>After all changes are done, press <strong>Submit</strong> button.</p>
		
		<figure class="img-polaroid">
			<img src="img/follow-settings-01.png" alt="Social sharing settings">
		</figure>
	</li>
</ol>

<p>Social icons per user can be configured the same way.</p>
<p>To configure social icons per user block you should edit the <strong>TM Follow User</strong> block.</p>
<p>To configure additional display parameters for social icons per user, follow the steps below:</p>
<ol class="index-list">
	<li>
		In the admin panel navigate to <strong>People</strong> menu or in a browser's address bar type <strong>http://www.example.com/?q=admin/people</strong>,
		where <strong>www.example.com</strong> is your website URL.
	</li>
	<li>
		Next to the needed user click <strong>edit</strong> link.
	</li>
	<li>
		Navigate to <strong>TM Follow</strong> tab.
	</li>
	<li>
		Perform the necessary changes and save them.
	</li>
</ol>