<h3>Using LazyLoad effect</h3>
<p class="alert alert-info">
	<span> The <strong>TM Lazy Load</strong> module controls the activation/deactivation of the lazyload effect for images.</span>
</p>
<p>To configure lazyload settings for images, follow the instructions below:</p>
<ol class="index-list">
	<li>
		In the admin panel navigate to <strong>Configuration > TM Lazyload</strong> menu or in a browser's address
		bar type <strong>http://www.example.com/?q=admin/config/tm_lazyload</strong>, where
		<strong>www.example.com</strong> is your website URL.
	</li>
	<li>
		<p>Perform the necessary changes in the following fields:</p>

		<ul class="marked-list">
			<li>
				<dl class="inline-term">
					<dt>Enabled</dt>
					<dd>enable/disable lazyload effect.</dd>
				</dl>
			</li>
			<li>
				<dl class="inline-term">
					<dt>Distance</dt>
					<dd>the distance (in pixels) of image to the viewable browser window before it loads the actual image.</dd>
				</dl>
			</li>
			<li>
				<dl class="inline-term">
					<dt>Placeholder Image</dt>
					<dd>path to your placeholder image. Leave it blank to use the default image.</dd>
				</dl>
			</li>
			<li>
				<dl class="inline-term">
					<dt>Loader Icon</dt>
					<dd>available loader icons.</dd>
				</dl>
			</li>
			<li>
				<dl class="inline-term">
					<dt>Excluded Pages</dt>
					<dd>list of the page paths to be excluded from lazyloading.</dd>
				</dl>
			</li>
		</ul>
	</li>
	<li>
		<p>After all changes are done, press <strong>Save</strong> button.</p>
		
		<figure class="img-polaroid">
			<img src="img/lazyload-settings-01.png" alt="LazyLoad settings">
		</figure>
	</li>
</ol>