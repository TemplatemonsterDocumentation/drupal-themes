<h3>Olark chat</h3>
<p class="alert alert-info">
    <span>The <strong>Olark Chat</strong> module controls the activation/deactivation of the Olark Live chat.</span>
</p>
<p>If you install Drupal theme with sample data, the Olark chat module will be enabled by default.</p>
<p>To set up the Olark chat as the one on the Live Demo, you should perform the following steps:</p>
<ol class="index-list">
    <li><p>Register <a href="https://www.olark.com/install" target="_blank">on the official Olark site</a> (check the
            screenshots that show next three steps, in the slider, after the point 3).</p>

        <figure class="img-polaroid">
            <img src="img/chart-01.png" alt="Register on olark.com">
        </figure>
    </li>
    <li><p>Copy the generated code.</p>

        <figure class="img-polaroid">
            <img src="img/chart-03.png" alt="Copy code">
        </figure>
    </li>
</ol>

<p class="alert alert-info">
    <span>You can also get a 180 day free trial of the 6 Month Olark Monster Deal! plan. Just follow this <a
            href="http://olark.com/signup/create_new_account/18981?utm_source=templatemonster&utm_medium=offer&utm_campaign=default">link</a> and fill in your personal data.</span>
</p>

<ol class="index-list">
    <li>In your Drupal admin panel navigate to the <strong>Configuration</strong> menu and in the <strong>Web
            services</strong> section select <strong>Olark settings</strong>, or in a browser's address bar type
        <strong> http://www.example.com/?q=admin/config/services/olark</strong>, where <strong>www.example.com</strong>
        is your website URL.
    </li>
    <li>Paste the copied code to the <strong>Olark code</strong> field.</li>
    <li><p>Click <strong>Save configuration </strong> button.</p>

        <figure class="img-polaroid">
            <img src="img/chart-04.png" alt="Paste code">
        </figure>
    </li>
    <li>The chat settings you can configure in your account on the official Olark site.</li>
</ol>
