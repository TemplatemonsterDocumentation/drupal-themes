<h3>Настройки блоков</h3>
<p>Чтобы настроить блоки, использованные в шаблоне, следуйте данным инструкциям:</p>
<ol class="index-list">
	<li>
		Войдите на сайт в качестве администратора.
	</li>
	<li>
		В панели администрирования зайдите на страницу <strong>Structure > Blocks (Структура > Блоки)</strong>
		или в адресной строке браузера наберите <strong>http://www.example.com/?q=admin/structure/block</strong>, где
		<strong>www.example.com</strong> - адрес вашего сайта.
	</li>
	<li>
		<p>На открывшейся странице вы можете перетаскивать блоки в разные регионы:</p>

		<figure class="img-polaroid">
			<img src="img/block-settings-01.png" alt="Drag&amp;Drop blocks">
		</figure>
	</li>
	<li>
		<p>Вы можете перемещать блоки в разные регионы, используя выпадающее меню в колонке <strong>Region
		(Регион)</strong>:</p>

		<figure class="img-polaroid">
			<img src="img/block-settings-02.png" alt="Select block region">
		</figure>
	</li>
	<li>
		<p>Вы можете задать настройки для каждого блока в отдельности, нажав на ссылку <strong>configure
		(настроить)</strong> в колонке <strong>Operations (Операции)</strong>:</p>

		<figure class="img-polaroid">
			<img src="img/block-settings-03.png" alt="Configure block">
		</figure>
	</li>
	<li>
		<p>На странице настроек блока вы можете задать следующие параметры:</p>

		<ul class="marked-list">
			<li>заголовок блока;</li>
			<li>пользовательский CSS-класс для данного блока;</li>
			<li>регион блока для каждой темы в отдельности;</li>
			<li>настройки отображения (для отдельных страниц, отдельных типов материалов, отдельных ролей пользователей, для каждого пользователя);</li>
			<li>настроить фон блока (аналогично настройкам регионов в опциях темы).</li>
		</ul>

		<figure class="img-polaroid">
			<img src="img/block-settings-04.png" alt="Block settings page">
		</figure>

		<p class="alert alert-warning">
			<span>Помните, что блок занимает ширину региона, в который он помещен. Для того, чтобы блок с эффектом параллакса или с видео на фоне занимал всю ширину экрана, необходимо поместить его в регион с опцией <strong>Fullwidth (На всю ширину экрана)</strong>.</span>
		</p>
		<p class="alert alert-info">
			<span>Некоторые блоки могут содержать дополнительные настройки.</span>
		</p>
	</li>
	<li>
		<p>Вы также можете попасть на страницу настроек блока со страниц вашего сайта. Для этого наведите курсор мышки на
		нужный вам блок, затем нажмите на пиктограмму шестеренки в правом верхнем углу блока и выберите пункт <strong>Configure
		block (Настроить блок)</strong>. Эта опция будет доступна только, если вы войдете на сайт в качестве
		администратора:</p>

		<figure class="img-polaroid">
			<img src="img/block-settings-05.png" alt="Click cog to configure block">
		</figure>
	</li>
</ol>