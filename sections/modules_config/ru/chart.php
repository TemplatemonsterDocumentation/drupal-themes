<h3>Olark чат</h3>
<p class="alert alert-info">
	<span>За включение/отключение Olark-чата отвечает модуль <strong>Olark Chat</strong>.</span>
</p>
<p>При установке шаблонов с демонстрационными данными модуль Olark Chat включен по умолчанию.</p>

<p>Для того, чтобы добавить Olark-чат, как на демо-странице шаблона, выполните следующие действия:</p>
<ol class="index-list">
	<li>
		<p>Зарегистрируйтесь на <a href="https://www.olark.com/install" target="_blank">официальном сайте Olark</a></p>

		<figure class="img-polaroid">
			<img src="img/chart-01.png" alt="Register on olark.com">
		</figure>
	</li>
	<li>
		<p>Скопируйте полученный код.</p>

		<figure class="img-polaroid">
			<img src="img/chart-03.png" alt="Copy code">
		</figure>
		
		<p class="alert alert-info">
			<span>Вы также можете получить пробный бесплатный 6-месячный бронзовый пакет. Для этого перейдите по <a href="http://olark.com/signup/create_new_account/18981?utm_source=templatemonster&utm_medium=offer&utm_campaign=default">ссылке</a> и внесите свои регистрационные данные.</span>
		</p>
	</li>
	<li>
		На своем сайте в панели администрирования зайдите на страницу <strong>Configuration (Настройки)</strong> и в
		разделе <strong>Web services (Web-сервисы)</strong> выберите <strong>Olark settings (Настройки Olark)</strong>.
		Вы также можете попасть на данную страницу, перейдя по ссылке <strong>http://www.example.com/?q=admin/config/services/olark</strong>,
		где <strong>www.example.com</strong> - адрес вашего сайта.
	</li>
	<li>
		Вставьте скопированный код в поле <strong>Olark code (Код Olark)</strong>
	</li>
	<li>
		<p>Нажмите на кнопку <strong>Save configuration (Сохранить настройки)</strong>.</p>

		<figure class="img-polaroid">
			<img src="img/chart-04.png" alt="Paste code">
		</figure>
	</li>
	<li>
		Для настройки чата зайдите на сайт Olark под своей учетной записью.
	</li>
</ol>

