<h3>Настройки темы</h3>
<p>Каждая тема имеет набор собственных настроек.</p>
<p>Чтобы изменить настройки темы, выполните следующие действия:</p>
<ol class="index-list">
	<li>
		В панели администрирования сайта зайдите на страницу <strong>Appearance (Оформление)</strong> либо в адресной
		строке браузера наберите <strong>http://www.example.com/?q=admin/appearance</strong>, где <strong>www.example.com</strong>
		- адрес вашего сайта.
	</li>
	<li>
		<p>Нажмите на ссылку <strong>Settings (Настройки)</strong> под именем вашей темы:</p>

		<figure class="img-polaroid">
			<img src="img/theme-settings-01.png" alt="Click theme settings">
		</figure>
	</li>
	<li>
		<p>На вкладке <strong>General Settings (Общие настройки)</strong> вы можете задать следующие параметры:</p>

		<ul class="marked-list">
			<li>отобразить/скрыть логотип, название сайта, слоган, аватары авторов постов, аватары авторов комментариев,
				состояние проверки авторов комментариев, иконку для избранного, главное и вспомогательное меню;</li>
			<li>использовать логотип по умолчанию (из темы) или загрузить собственный;</li>
			<li>использовать иконку для избранного (фавикон) по умолчанию (из темы) или загрузить собственную;</li>
			<li>закрепить меню вверху окна браузера.</li>
		</ul>

		<figure class="img-polaroid">
			<img src="img/theme-settings-02.png" alt="General settings">
		</figure>
	</li>
	<li>
		<p>На вкладке <strong>Breadcrumb Settings (Настройки "хлебных крошек")</strong> вы можете задать следующие
			параметры:</p>

		<ul class="marked-list">
			<li>отобразить/скрыть "хлебные крошки";</li>
			<li>отобразить/скрыть "хлебные крошки", если они содержат только ссылку на главную страницу сайта;</li>
			<li>задать разделитель между ссылками "хлебных крошек".</li>
		</ul>

		<figure class="img-polaroid">
			<img src="img/theme-settings-03.png" alt="Breadcrumb Settings">
		</figure>
	</li>
	<li>
		<p>На вкладке <strong>Regions Settings (Настройки регионов)</strong> вы можете задать следующие настройки:</p>

		<ul class="marked-list">
			<li>задать изображение на фон региона, а также включить эффект параллакса;</li>
			<li>задать YouTube-видео на фон региона, а также задать, с какой секунды необходимо воспроизводить видео;
			</li>
			<li>растянуть содержимое блока на всю ширину экрана (т.е. убрать грид-сетку).</li>
		</ul>

		<p class="alert alert-danger">
			<span>Некоторые из данных опций работают только с включенным модулем <strong>TM Block Background</strong>.</span>
		</p>

		<figure class="img-polaroid">
			<img src="img/theme-settings-06.png" alt="Regions Settings">
		</figure>
	</li>
	<li>
		<p>На вкладке <strong>Blog Settings (Настройки блога)</strong> вы можете задать заголовок блока. Оставьте данное
		поле пустым, чтобы заголовок блога совпадал с названием соответствующей ссылки меню.</p>

		<figure class="img-polaroid">
			<img src="img/theme-settings-04.png" alt="Blog Settings">
		</figure>
	</li>
	<li>
		<p>На вкладке <strong>Custom CSS (Пользовательский CSS)</strong> вы можете задать собственные стили для
		установленной темы.</p>

		<p class="alert alert-warning">
			<span>Данная опция относится только к приобретенной вами теме. Вы не сможете задать стили для других тем, таких как Seven и прочих.</span>
		</p>

		<figure class="img-polaroid">
			<img src="img/theme-settings-05.png" alt="Custom CSS">
		</figure>
	</li>
</ol>