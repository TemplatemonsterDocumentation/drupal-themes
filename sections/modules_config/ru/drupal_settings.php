<h3>Настройки сайта</h3>
<p>Просмотреть полный список настроек вы можете зайдя на страницу <strong>Configuration (Настройки)</strong> панели
    администрирования сайта либо набрав в адресной строке браузера
    <strong>http://www.example.com/?q=admin/config</strong>, где <strong>www.example.com</strong> - адрес вашего сайта.
</p>
<p>Чтоб изменить основные настройки сайта, выполните следующие инструкции:</p>
<ol class="index-list">
    <li>Зайдите на страницу <strong>Configuration > System > Site information (Настройки > Система >
            Информация о сайте)</strong> панели администрирования сайта либо наберите в адресной строке браузера
        <strong>http://www.example.com/?q=admin/config/system/site-information</strong>, где
        <strong>www.example.com</strong> - адрес вашего сайта.
    </li>
    <li>
        <p>Измените необходимые параметры:</p>

        <ul class="marked-list">
            <li>название сайта;</li>
            <li>слоган сайта;</li>
            <li>электронный адрес сайта;</li>
            <li>количество постов на странице блога (или на главной странице, если у вас есть посты, прикрепленные к
                главной);
            </li>
            <li>адрес главной страницы сайта;</li>
            <li>адрес страницы 403 (доступ запрещен);</li>
            <li>адрес страницы 404 (страница не найдена).</li>
        </ul>
        <figure class="img-polaroid">
            <img src="img/drupal-settings-01.png" alt="Set site name">
        </figure>
    </li>
    <li>После внесения необходимых изменений, нажмите кнопку <strong>Save configuration (Сохранить настройки)</strong>.
    </li>
</ol>