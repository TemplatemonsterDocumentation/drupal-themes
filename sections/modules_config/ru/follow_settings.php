<h3>Настройки блока социальных ссылок</h3>
<p class="alert alert-info">
    <span>За включение/отключение блока социальных ссылок отвечают модули <strong>TM Font Awesome</strong> и <strong>TM
            Follow Us</strong>.</span>
</p>
<p>Для настройки параметров блока социальных ссылок выполните следующие действия:</p>
<ol class="index-list">
    <li>
        <p>В панели администрирования зайдите на страницу <strong>Structure > Blocks (Структура >
                Блоки)</strong> или в адресной строке браузера наберите <strong>http://www.example.com/?q=admin/structure/block</strong>,
            где <strong>www.example.com</strong> - адрес вашего сайта.</p>

        <p>Зайти в данные настройки также возможно, выбрав пункт <strong>Configure block (Настроить блок)</strong>
            контекстного меню блока социальных ссылок.</p>

        <figure class="img-polaroid">
            <img src="img/follow-settings-03.png" alt="Choose 'Configure block'">
        </figure>
    </li>
    <li>Чтобы отобразить/скрыть данный блок на страницах пользователей, поставьте/уберите галочку в поле <strong>User
            pages (Страницы пользователей)</strong>.
    </li>
    <li>Чтобы отобразить/скрыть подписи к социальным сетям, поставьте/уберите галочку в поле <strong>Display labels
            (Отображать метки)</strong>.
    </li>
    <li>Чтобы отобразить ссылки вертикально или горизонтально, выберите соответствующий пункт в поле <strong>Alignment
            (Выравнивание)</strong>.
    </li>
    <li>После того, как все изменения будут сделаны, нажмите кнопку <strong>Save block (Сохранить блок)</strong>.</li>
</ol>
<figure class="img-polaroid">
    <img src="img/follow-settings-04.png" alt="Social sharing settings">
</figure>
<p>Для настройки дополнительных параметров отображения социальных ссылок выполните следующие действия:</p>
<ol class="index-list">
    <li>
        <p>В панели администрирования зайдите на страницу <strong>Configuration > TM Follow (Настройка > TM
                Follow)</strong> или в адресной строке браузера наберите <strong>http://www.example.com/?q=admin/config/tm_follow</strong>,
            где <strong>www.example.com</strong> - адрес вашего сайта.</p>

        <p>Зайти в данные настройки также возможно, выбрав пункт <strong>Edit TM Follow links (Редактировать ссылки TM
                Follow)</strong> контекстного меню блока социальных ссылок.</p>

        <figure class="img-polaroid">
            <img src="img/follow-settings-02.png" alt="Choose 'Edit TM Follow links'">
        </figure>
    </li>
    <li>Чтобы скрыть какую-либо из иконок социальных сетей, уберите ссылку из поля <strong>URL</strong> напротив
        необходимой социальной сети.
    </li>
    <li>Чтобы показать какую-либо из иконок социальных сетей, укажите ссылку на свой профиль в социальной сети в поле
        <strong>URL</strong>.
    </li>
    <li>Чтобы изменить значение подписи социальной сети, укажите новое значение в поле <strong>Customized name
            (Измененное имя)</strong> напротив необходимой социальной сети.
    </li>
    <li>После того, как все изменения будут сделаны, нажмите кнопку <strong>Submit (Подтвердить)</strong>.</li>
</ol>
<figure class="img-polaroid">
    <img src="img/follow-settings-01.png" alt="Social sharing settings">
</figure>
<p>Социальные ссылки отдельного пользователя настраиваются аналогичным образом.</p>
<p>Для настройки блока социальных ссылок пользователя отредактируйте блок <strong>TM Follow User</strong>.</p>
<p>Для настройки дополнительных параметров отображения социальных ссылок отдельного пользователя выполните следующие
    действия:</p>
<ol class="index-list">
    <li>В панели администрирования зайдите на страницу <strong>People (Люди)</strong> или в адресной строке браузера
        наберите <strong>http://www.example.com/?q=admin/people</strong>, где <strong>www.example.com</strong> - адрес
        вашего сайта.
    </li>
    <li>Напротив необходимого пользователя нажмите ссылку <strong>edit (редактировать)</strong>.</li>
    <li>Перейдите на вкладку <strong>TM Follow</strong>.</li>
    <li>Сделайте необходимые изменения и сохраните их.</li>
</ol>