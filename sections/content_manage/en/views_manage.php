<h3>View management</h3>
<p>Most Drupal templates content is made using views, such as:</p>
<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Archive</dt>
            <dd>takes the content type "Blog entry", arranges them by months and displays them in the block or page
                mode.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Banners</dt>
            <dd>takes the content type "Banner" and displays them in the block mode.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Categories</dt>
            <dd>takes taxonomy terms from the
                <em>"Blog categories"</em>
                vocabulary and displays them in the block mode.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>FAQs</dt>
            <dd>takes the content type "FAQs" and displays them in the page mode.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Portfolio</dt>
            <dd>takes the content type "Portfolio Item" and displays them in the Portfolio (Gallery, Services) page
                mode in two, three or four columns.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Portfolio filter</dt>
            <dd>creates a portfolio filter block that can be used in the "Portfolio" view.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Recent comments</dt>
            <dd>takes comments and displays them in the block or page mode.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Recent posts</dt>
            <dd>takes the content type "Blog entry" and displays them in the block mode.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Slider</dt>
            <dd>takes the content type "Slide" and displays them in the slider block mode.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Team</dt>
            <dd>takes the content type "Employee" and displays them in the block mode.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Testimonials</dt>
            <dd>takes the content type "Testimonial" and displays them in the block or page mode.</dd>
        </dl>
    </li>
    <li>And other views.</li>
</ul>
<p>To manage views, follow the instructions below:</p>
<ol class="index-list">
    <li>To check the full list of all existing views, in the admin panel navigate to <strong>Structure >
            Views</strong> menu or in a browser's address bar type <strong>http://www.example.com/?q=admin/structure/views</strong>,
        where <strong>www.example.com</strong> is your website URL.
    </li>
    <li><p>To create a new view, click <strong>Add new view</strong> link.</p>

        <figure class="img-polaroid">
            <img src="img/views-manage-01.png" alt="Click 'Add new view'">
        </figure>
    </li>
    <li>On the appeared page specify a view title, content type, ordering type and display mode.</li>
    <li><p>To set additional parameters, press <strong>Continue &amp; edit</strong> button. To complete, press <strong>Save
                &amp; exit</strong> button.</p>

        <figure class="img-polaroid">
            <img src="img/views-manage-02.png" alt="Fill view data">
        </figure>
    </li>
    <li><p>To edit an existing view, select <strong>Edit</strong> option from the drop-down list next to the view
            title.</p>

        <figure class="img-polaroid">
            <img src="img/views-manage-03.png" alt="Click 'Edit'">
        </figure>
    <p class="alert alert-warning">
        <span>Do not make changes to an existing view, if you are not sure what you are doing.</span>
    </p>
    <p></p>
    </li>
    <li><p>To remove an existing view, select <strong>Delete</strong> option from the drop-down list next to the view
            title.</p>

        <figure class="img-polaroid">
            <img src="img/views-manage-04.png" alt="Click 'Delete'">
        </figure>
    </li>
    <li><p>Views displayed in the block mode can be managed the same way as the regular blocks. You can also open a
            view
            block's settings page directly from your site front-end. To do it, mouse over the needed block, click the
            gear
            wheel icon at the top right corner of the block and choose the <strong>Configure block</strong> option:</p>

        <figure class="img-polaroid">
            <img src="img/views-manage-05.png" alt="Click 'Edit view'">
        </figure>
    </li>
    <li><p>Views displayed in the pages mode can be set up the same way. Mouse over the needed block, click the gear
            wheel
            icon at the top right corner of the block and choose the <strong>Edit view</strong> option:</p></li>
</ol>
<figure class="img-polaroid">
    <img src="img/views-manage-06.png" alt="Click 'Edit view'">
</figure>
