<h3>Page management</h3>
<p>All template pages are created using the <strong>Panels</strong> module.</p>
<p>To manage pages, follow the instructions below:</p>

<p>To check the list of all existing pages, in the admin panel navigate to <strong>Structure > Pages</strong> menu
    or in a browser's address bar type <strong>http://www.example.com/?q=admin/structure/pages</strong>, where <strong>www.example.com</strong>
    is your website URL.</p>
<p>To create a new page, click <strong>Add custom page</strong> link.</p>

<figure class="img-polaroid">
    <img src="img/page-manage-01.png" alt="Click 'Add custom page'">
</figure>

<p>Follow the instructions on the screen, fill in all required fields, save the changes.</p>
<p>To edit an existing page, click <strong>Edit</strong> link next to the page title.</p>

<figure class="img-polaroid">
    <img src="img/page-manage-02.png" alt="Click 'Edit'">
</figure>

<p>To remove a page, in the editing mode click <strong>Delete </strong> tab at the top of the page.</p>

<figure class="img-polaroid">
    <img src="img/page-manage-03.png" alt="Click 'Delete'">
</figure>
