<h3>Media management</h3>
<p><strong>Media</strong> module controls the uploading of images and videos.</p>
<p>To manage media, follow the instructions below:</p>
<ol class="index-list">
    <li>To check the full list of all existing media files, in the admin panel open <strong>Content</strong> menu and
        navigate to <strong>Media</strong> tab or in a browser's address bar type <strong>http://www.example.com/?q=admin/content/media</strong>,
        where <strong>www.example.com</strong> is your website URL.
    </li>
    <li><p>To remove existing images or videos, check them, in the <strong>Operations </strong> field choose <strong>Delete </strong>
            option and click <strong>Submit </strong> button.</p>

        <figure class="img-polaroid">
            <img src="img/media-manage-01.png" alt="Delete media files">
        </figure>
    </li>
    <li><p>To add a media file to a node, click <strong>Select media</strong> button in the node editing mode.</p>

        <figure class="img-polaroid">
            <img src="img/media-manage-02.png" alt="Click 'Select media'">
        </figure>
    </li>
    <li>
        <p>On the appeared page:</p>

        <ul class="marked-list">
            <li>in the <strong>Upload</strong> tab you can upload an image from your computer;</li>
            <li>in the <strong>Web</strong> tab you can specify an image URL or a YouTube or Vimeo video URL;</li>
            <li>in the <strong>Library</strong> tab you can select an image or video from the Drupal Media library.</li>
        </ul>
        <p>After you select the media file, press <strong>Submit</strong> button.</p>

        <figure class="img-polaroid">
            <img src="img/media-manage-03.png" alt="Chose media">
        </figure>
        <p class="alert alert-warning">Please, note, that you can add a video only to the
            <em>Blog entry</em>
            content type nodes.
        </p>
        <p></p>
    </li>
</ol>
