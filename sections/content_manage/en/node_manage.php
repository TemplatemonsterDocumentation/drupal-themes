<h3>Content type management</h3>
<p>Drupal templates include the following main content types:</p>
<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Banner</dt>
            <dd>represents company name, logo and additional information. It can be used in blocks like "Our partners",
                "Our clients", etc.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Employee</dt>
            <dd>consists of an employeeâ€™s photo and a summary regarding the employee. It can be used in blocks like
                "Our team", "Our staff", etc.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>FAQ</dt>
            <dd>represents content in question-answer form. Such content type is being used on FAQ pages.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Portfolio item</dt>
            <dd>consists of a title, an image and some information. Such content type is being used on pages like
                "Portfolio", "Gallery", etc.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Service</dt>
            <dd>consists of a title, an image and and some information. It can be used in blocks like "Services",
                etc.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Slide</dt>
            <dd>consists of a title, an image and some information. It is being used in the slider on the Home page.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Testimonial</dt>
            <dd>consists of a short feedback and some information about its author. Can be used in "Testimonials",
                "What people say about us" blocks, etc.
            </dd>
        </dl>
    </li>
    <li>Other content types.</li>
</ul>
<p class="alert alert-warning">
    <span>Any changes made in these content types (adding, editing, deleting) instantly appear in the blocks or on the pages where they are used.</span>
</p>
<p>Some of the modules contain their own content types. For example, the Panels module adds
    <em>Panel</em>
    content type.
</p>
<p>You can also create custom content types.</p>
<p>To edit, delete or manage a content type, follow the instructions below:</p>
<ol class="index-list">
    <li>To check the full list of the existing content types, in the admin panel navigate to <strong>Content</strong>
        menu or in a browser's address bar type <strong>http://www.example.com/?q=admin/content</strong>, where <strong>www.example.com</strong>
        is your website URL.
    </li>
    <li><p>To create a new node, click <strong>Add content</strong> link.</p>

        <figure class="img-polaroid">
            <img src="img/node-manage-01.png" alt="Click 'Add content'">
        </figure>
    </li>
    <li><p>On the appeared page select a content type.</p>

        <figure class="img-polaroid">
            <img src="img/node-manage-02.png" alt="Chose content type">
        </figure>
    </li>
    <li>Fill in the required fields and click <strong>Save</strong> button.</li>
    <li><p>To edit an existing node, on the <strong>Content</strong> page in the <strong>Show only items where</strong>
            field select a content type and click <strong>Filter</strong> button.</p>

        <figure class="img-polaroid">
            <img src="img/node-manage-03.png" alt="Filter content">
        </figure>
    </li>
    <li><p>To edit a node of the content type, click <strong>Edit</strong> link next to the node title.</p>

        <figure class="img-polaroid">
            <img src="img/node-manage-04.png" alt="Click 'Edit'">
        </figure>
    </li>
    <li><p>To remove a node, click <strong>Delete</strong> link next to the node title.</p>

        <figure class="img-polaroid">
            <img src="img/node-manage-05.png" alt="Click 'Delete'">
        </figure>
    </li>
</ol>
