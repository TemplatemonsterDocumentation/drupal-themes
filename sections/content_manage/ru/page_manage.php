<h3>Управление страницами</h3>
<p>Все страницы шаблона созданы с помощью модуля <strong>Panels (Панели)</strong>.</p>
<p>Для управления страницами выполните следующие действия:</p>

<p>Чтоб просмотреть все существующие страницы, в панели администрирования зайдите на страницу <strong>Structure >
	Pages (Структура > Страницы)</strong> или в адресной строке браузера наберите <strong>http://www.example.com/?q=admin/structure/pages</strong>,
	где <strong>www.example.com</strong> - адрес вашего сайта.</p>
<p>Чтоб создать страницу, вверху открывшейся страницы нажмите ссылку <strong>Add custom page (Добавить пользовательскую
	страницу)</strong>.</p>

<figure class="img-polaroid">
	<img src="img/page-manage-01.png" alt="Click 'Add custom page'">
</figure>

<p>Следуйте инструкциям на экране, заполните все необходимые поля, сохраните внесенные данные.</p>
<p>Чтобы отредактировать существующую страницу, нажмите на ссылку <strong>Edit (Редактировать)</strong> напротив нужного
	заголовка.</p>

<figure class="img-polaroid">
	<img src="img/page-manage-02.png" alt="Click 'Edit'">
</figure>

<p>Чтобы удалить страницу, в режиме редактирования нажмите на ссылку <strong>Delete (Удалить)</strong> вверху страницы.</p>

<figure class="img-polaroid">
	<img src="img/page-manage-03.png" alt="Click 'Delete'">
</figure>

