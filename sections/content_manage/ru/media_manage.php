<h3>Управление медиа-содержимым</h3>
<p>Загрузка изображений и видео в шаблоне осуществляется по средствам модуля <strong>Media (Медиа)</strong>.</p>
<p>Для управления медиа-содержимым выполните следующие действия:</p>
<ol class="index-list">
	<li>
		Чтоб просмотреть полный список медиа-файлов, в панели администрирования зайдите на страницу <strong>Content
		(Материалы)</strong> и перейдите на вкладку <strong>Media (Медиа)</strong> или в адресной строке браузера
		наберите <strong>http://www.example.com/?q=admin/content/media</strong>, где <strong>www.example.com</strong> -
		адрес вашего сайта.
	</li>
	<li>
		<p>Чтобы удалить существующие изображения или видео, отметьте их, в появившемся поле <strong>Operations
		(Операции)</strong> выберите <strong>Delete (Удалить)</strong> и нажмите кнопку <strong>Submit
		(Подтвердить)</strong>.</p>

		<figure class="img-polaroid">
			<img src="img/media-manage-01.png" alt="Delete media files">
		</figure>
	</li>
	<li>
		<p>Для того, чтобы добавить медиа-файл в материал, в режиме создания или редактирования материала нажмите
		кнопку <strong>Select media (Выбрать медиа)</strong>.</p>

		<figure class="img-polaroid">
			<img src="img/media-manage-02.png" alt="Click 'Select media'">
		</figure>
	</li>
	<li>
		<p>В появившемся окне:</p>

		<ul class="marked-list">
			<li>
				во вкладке <strong>Upload (Загрузить)</strong> вы можете загрузить изображение с вашего компьютера;
			</li>
			<li>
				во вкладке <strong>Web</strong> вы можете указать ссылку на изображение, размещенное в Интернете, а
				также ссылку на видео с сайтов YouTube и Vimeo;
			</li>
			<li>
				во вкладке <strong>Library (Библиотека)</strong> вы можете выбрать одно из изображений или видео, уже
				загруженных на ваш сайт.
			</li>
		</ul>
		<p>После выбора медиа-файла нажмите кнопку <strong>Submit (Подтвердить)</strong>.</p>

		<figure class="img-polaroid">
			<img src="img/media-manage-03.png" alt="Chose media">
		</figure>

		<p class="alert alert-warning">
			<span>Пожалуйста, примите к сведению, что добавить видео вы сможете только для типа материала <em>Blog entry (Запись блога)</em>.</span>
		</p>
	</li>
</ol>

