<h3>Управление материалами</h3>
<p>В шаблоне присутствуют следующие основные типы материалов:</p>
<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Banner (Баннер)</dt>
            <dd>представляет собой название компании, логотип и дополнительную информацию. Может использоваться в
                блоках "Our partners" (Наши партнеры), "Our clients" (Наши клиенты) и т.п.;
            </dd>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Employee (Сотрудник)</dt>
            <dd>представляет собой фотографию сотрудника и короткую информацию о нем. Может использоваться в блоках
                "Our team" (Наша команда), "Our staff" (Наша персонал) и т.п.;
            </dd>
    </li>
    <li>
        <dl class="inline-term">
            <dt>FAQ (ЧАВО)</dt>
            <dd>представляет собой материал формата "вопрос-ответ". Используется на странице "Часто задаваемые
                вопросы";
            </dd>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Portfolio item (Элемент портфолио)</dt>
            <dd>представляет собой заголовок, изображение и краткую информацию. Используется на страницах "Portfolio"
                (Портфолио), "Gallery" (Галерея), "Services" (Услуги) и т.п.;
            </dd>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Service (Услуга)</dt>
            <dd>представляет собой заголовок, изображение и краткую информацию. Может использоваться в блоках
                "Services" (Услуги) и т.п.;
            </dd>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Slide (Слайд)</dt>
            <dd>представляет собой заголовок, изображение и краткую информацию. Используется в слайдере на главной
                странице;
            </dd>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Testimonial (Отзыв)</dt>
            <dd>представляет собой кроткий отзыв и информацию о его авторе. Может использоваться в блоках
                "Testimonials" (Отзывы), "What people say about us" (Что о нас говорят) и т.п.;
            </dd>
    </li>
    <li>и прочие.</li>
</ul>
<p class="alert alert-warning">
    <span>Изменения данных типов материалов (добавление, редактирование, удаление) сразу же отображаются в блоках и на страницах, где они используются.</span>
</p>
<p>Некоторые модули могут добавлять свои типы материалов. К примеру, модуль Panels добавляет тип материала
    <em>Panel (Панель)</em>
    .
</p>
<p>Вы также можете создавать собственные типы материала.</p>
<p>Для изменения, удаления или управления материалом выполните следующие действия:</p>
<ol class="index-list">
    <li>Чтобы просмотреть полный список материалов, в панели администрирования зайдите на страницу <strong>Content
            (Материалы)</strong> или в адресной строке браузера наберите
        <strong>http://www.example.com/?q=admin/content</strong>, где <strong>www.example.com</strong> - адрес вашего
        сайта.
    </li>
    <li><p>Чтобы создать материал, нажмите на ссылку <strong>Add content (Добавить материал)</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/node-manage-01.png" alt="Click 'Add content'">
        </figure>
    </li>
    <li><p>На открывшейся странице выберите тип материала.</p>

        <figure class="img-polaroid">
            <img src="img/node-manage-02.png" alt="Chose content type">
        </figure>
    </li>
    <li>Заполните необходимые поля и нажмите кнопку <strong>Save (Сохранить)</strong>.</li>
    <li><p>Чтобы отредактировать существующий материал, на странице <strong>Content (Материалы)</strong> в поле
            <strong>Show
                only items where (Показать только элементы, где)</strong> выберите тип материала и нажмите кнопку
            <strong>Filter
                (Отфильтровать)</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/node-manage-03.png" alt="Filter content">
        </figure>
    </li>
    <li><p>Чтобы отредактировать материал, нажмите ссылку <strong>Edit (Редактировать)</strong> напротив его названия.
        </p>

        <figure class="img-polaroid">
            <img src="img/node-manage-04.png" alt="Click 'Edit'">
        </figure>
    </li>
    <li><p>Чтобы удалить материал, нажмите ссылку <strong>Delete (Удалить)</strong> напротив его названия.</p>

        <figure class="img-polaroid">
            <img src="img/node-manage-05.png" alt="Click 'Delete'">
        </figure>
    </li>
</ol>
