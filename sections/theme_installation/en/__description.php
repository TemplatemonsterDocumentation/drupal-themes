<h2 class="item2">Drupal engine and theme installation</h2>
<p>In this section you will see different ways of Drupal engine and theme installation. Please, follow this instruction
    if the Drupal is not installed to your server yet.</p>
<p>Before you get started, you need to decide if you want to install Drupal with sample data included or not. Template
    sample data will make your website look exactly the same way as the template demo. You will have all sample modules,
    text-blocks, media files installed.</p>
<p>If you are not ready to install sample data, please proceed to the step "Drupal theme installation without sample
    data". You can also install it later. Please, check "Drupal theme installation with sample data over the existing
    website" section.</p>