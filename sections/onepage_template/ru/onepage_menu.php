<p>После того, как все изменения в меню внесены, перейдите на страницу <strong>Structure → Blocks (Структура →
        Блоки)</strong> или в адресной строке браузера наберите
    <strong>http://www.example.com/?q=admin/structure/block</strong>, где <strong>www.example.com</strong> - адрес
    вашего сайта.</p>
<p>Переместите блок <strong>(Superfish)</strong> в регион <strong>Menu</strong>.</p>
<p>Проверьте, чтобы в настройках данного блока в поле <strong>Menu parent (Родительское меню)</strong> был выбран пункт
    <strong>&lt;Main menu&gt;</strong>.</p>
