<h3>Menu</h3>
<p class="alert alert-warning">
    <span>Убедитесь, что модуль <strong>Superfish</strong> включен.</span>
</p>
<p>После того, как все блоки размещены в нужные регионы, перейдите к редактированию главного меню (Main menu).</p>
<p>Удаление и изменения порядка пунктов меню осуществляется так же, как и в обычном меню.</p>
<p>Чтобы добавить/изменить пункт меню, в поле <strong>Path (Путь)</strong> введите ссылку вида <strong>&lt;front&gt;#block-id</strong>,
    где <strong>#block-id</strong> - id блока или региона.</p>
<p>Вы можете воспользоваться следующими id:</p>
