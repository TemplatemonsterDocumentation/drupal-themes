<h3>Содержимое</h3>
<p>Чтобы внести изменения в содержимое шаблона, в панели администрирования зайдите на страницу <strong>Structure →
        Blocks (Структура → Блоки)</strong> или в адресной строке браузера наберите <strong>http://www.example.com/?q=admin/structure/block</strong>,
    где <strong>www.example.com</strong> - адрес вашего сайта.</p>
<p>Разместите блоки в нужные регионы и сохраните настройки.</p>
<p class="alert alert-warning">
    <span>Проследите, чтобы все необходимые блоки были размещены на Главной.</span>
</p>
<p>Чтобы разместить блок только на главной, выполните следующие действия:</p>
<ol class="index-list">
    <li>В режиме редактирования блока перейдите к Visibility settings (Настройки видимости) и переключитесь на вкладку
        <strong>Pages (Страницы)</strong>.
    </li>
    <li>В поле <strong>Show block on specific pages (Показать блок на определенных страницах)</strong> выберите пункт
        <strong>Only the listed pages (Только на перечисленных страницах)</strong>.
    </li>
    <li>Здесь же в текстовом поле введите <strong>&lt;front&gt;</strong></li>
</ol>
