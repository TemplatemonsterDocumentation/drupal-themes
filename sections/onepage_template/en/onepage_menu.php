<h3>Menu</h3>
<p class="alert alert-warning">
	<span>Make sure that <strong>Superfish</strong> module is enabled.</span>
</p>
<p>After placing all the blocks to needed regions, you can proceed with Main menu editing.</p>
<p>You can delete and change menu items order same way, as in regular menu.</p>
<p>In order to add/edit menu item, type link like <strong>&lt;front&gt;#block-id</strong> in <strong>Path </strong>field,
	where <strong>#block-id</strong> - block or region id.</p>
